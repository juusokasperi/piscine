#include <unistd.h>

int	ft_str_is_lowercase(char *str)
{
	while (*str != '\0')
	{
		if (!(*str >= 'a' && *str <= 'z'))
		{
			return (0);
		}
		str = str + 1;
	}
	return (1);
}

void	main(void)
{
	char	*str;
	int	b;
	char	c;

	str = "abcdA";
	b = ft_str_is_lowercase(str);
	c = b + '0';
	write(1, &c, 1);
}
