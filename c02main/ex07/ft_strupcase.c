#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] = str[i] - 32;
		}
		i++;
	}
	return (str);
}

void	main(void)
{
	char	str[] = "abcdefghijklmnopqrstuvwxyz";
	char	*b;

	b = ft_strupcase(str);
	write(1, str, 26);
}
