#include <unistd.h>
#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] != '\0')
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (s1[i] - s2[i]);
}

int	main(void)
{
	char	str1[] = "a";
	char	str2[] = "\n";
	int		i;

	i = ft_strcmp(str1, str2);
	printf("%d\n", i);
	return (0);
}
