#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}

void	main(void)
{
	char	str[] = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
	char	*b;

	b = ft_strlowcase(str);
	write(1, str, 26);
}
